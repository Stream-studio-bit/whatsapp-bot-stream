/**
 * üî• NOVA FUN√á√ÉO: Salva hist√≥rico de conversa (chamada pelo messageHandler)
 * Armazena mensagens trocadas em mem√≥ria (pode evoluir para salvar em arquivo ou DB)
 * @param {string} jid - JID do WhatsApp
 * @param {string} message - Mensagem enviada ou recebida
 * @param {'in'|'out'} direction - Dire√ß√£o da mensagem
 */
export function saveConversationHistory(jid, message, direction = 'in') {
  const phone = extractPhoneNumber(jid);
  const key = `history_${phone}`;
  const existing = userCache.get(key) || [];
  const entry = {
    timestamp: new Date().toISOString(),
    direction,
    message
  };
  existing.push(entry);
  userCache.set(key, existing);
  
  if (process.env.DEBUG_MODE === 'true') {
    log('INFO', `üí¨ Hist√≥rico salvo para ${phone} (${direction}): ${message}`);
  }
}

