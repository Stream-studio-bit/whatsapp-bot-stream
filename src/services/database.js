/**
 * üî• MELHORADA: Bloqueia o bot para um usu√°rio (atendimento manual assumido)
 * @param {string} jid - JID do WhatsApp
 */
export function blockBotForUser(jid) {
  const phone = extractPhoneNumber(jid);
  const blockedAt = new Date();
  
  // üî• CORRE√á√ÉO: manualAttendanceCache √© a FONTE √öNICA DE VERDADE
  manualAttendanceCache.set(phone, {
    blockedAt: blockedAt, // Date object, n√£o string
    blockedBy: process.env.OWNER_NAME || 'Roberto'
  });
  
  // Atualiza userCache apenas para manter sincronizado (mas manualAttendanceCache √© a fonte)
  const user = userCache.get(phone);
  if (user) {
    user.blockedAt = blockedAt;
    userCache.set(phone, user);
  }
  
  log('WARNING', `üö´ Bot bloqueado para: ${phone} (atendimento manual)`);
}

/**
 * üî• MELHORADA: Libera o bot para um usu√°rio (volta para autom√°tico)
 * @param {string} jid - JID do WhatsApp
 */
export function unblockBotForUser(jid) {
  const phone = extractPhoneNumber(jid);
  
  // üî• CORRE√á√ÉO: Remove do manualAttendanceCache (fonte √∫nica)
  manualAttendanceCache.del(phone);
  
  // Sincroniza userCache
  const user = userCache.get(phone);
  if (user) {
    user.blockedAt = null;
    userCache.set(phone, user);
  }
  
  log('SUCCESS', `‚úÖ Bot liberado para: ${phone} (autom√°tico novamente)`);
}

/**
 * üî• MELHORADA: Verifica se o bot est√° bloqueado para um usu√°rio
 * @param {string} jid - JID do WhatsApp
 * @returns {boolean}
 */
export function isBotBlockedForUser(jid) {
  const phone = extractPhoneNumber(jid);
  
  // üî• CORRE√á√ÉO: Verifica no manualAttendanceCache (fonte √∫nica)
  const manualAttendance = manualAttendanceCache.get(phone);
  
  if (!manualAttendance) {
    return false; // N√£o est√° bloqueado
  }
  
  // üî• NOVO: Verifica se bloqueio expirou
  if (isBlockExpired(manualAttendance.blockedAt)) {
    // Desbloqueia automaticamente
    unblockBotForUser(jid);
    
    if (process.env.DEBUG_MODE === 'true') {
      log('INFO', `‚è∞ Bloqueio expirado e removido para: ${phone}`);
    }
    
    return false;
  }
  
  return true; // Est√° bloqueado e n√£o expirou
}

/**
 * Lista todos os usu√°rios bloqueados (em atendimento manual)
 * @returns {Array}
 */
export function getBlockedUsers() {
  const keys = manualAttendanceCache.keys();
  return keys.map(key => ({
    phone: key,
    ...manualAttendanceCache.get(key)
  }));
}

/**
 * üî• NOVA FUN√á√ÉO: Limpa bloqueios expirados (chamada periodicamente)
 * @returns {number} Quantidade de bloqueios removidos
 */
export function cleanExpiredBlocks() {
  const keys = manualAttendanceCache.keys();
  let cleaned = 0;
  
  keys.forEach(phone => {
    const attendance = manualAttendanceCache.get(phone);
    if (attendance && isBlockExpired(attendance.blockedAt)) {
      manualAttendanceCache.del(phone);
      
      // Sincroniza userCache
      const user = userCache.get(phone);
      if (user) {
        user.blockedAt = null;
        userCache.set(phone, user);
      }
      
      cleaned++;
      log('INFO', `üßπ Bloqueio expirado removido: ${phone}`);
    }
  });
  
  if (cleaned > 0) {
    log('SUCCESS', `‚úÖ ${cleaned} bloqueio(s) expirado(s) removido(s)`);
  }
  
  return cleaned;
}

